def celsius_para_fahrenheit(x):
    return 1.8*x + 32

def fahrenheit_para_celsius(x):
    return (x - 32)/1.8

def metro_para_pes(x):
    return 3.28084 * x

def pes_para_metro(x):
    return x / 3.28084

def quilometro_para_milhas(x):
    return x / 1.609344

def milhas_para_quilometro(x):
    return x * 1.609344

def dia_para_horas(x):
    return x*24

